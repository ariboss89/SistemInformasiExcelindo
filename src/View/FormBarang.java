    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Id = txtId.getText().trim();
        nama = txtNamaBarang.getText().trim();
        kategori = cbKategori.getSelectedItem().toString().trim();
        stok = 0;
        hargabeli = Integer.parseInt(txtHargaBeli.getText().trim());
        hargajual = Integer.parseInt(txtHargaJual.getText().trim());
        minstok = Integer.parseInt(txtMinimum.getText().trim());
        
        if(Id.equals("")){
            JOptionPane.showMessageDialog(null, "Tidak Boleh Kosong");
            txtId.requestFocus();
        }
        else if(nama.equals("")){
            JOptionPane.showMessageDialog(null, "Tidak Boleh Kosong");
            txtNamaBarang.requestFocus();
        }
        else if(cbKategori.equals("PILIH")){
            JOptionPane.showMessageDialog(null, "Tidak Boleh Kosong");
            cbKategori.requestFocus();
        }
        else if(minstok==0){
            JOptionPane.showMessageDialog(null, "Tidak Boleh Kosong");
            txtMinimum.requestFocus();
        }
        else{
            barang.Save(Id, nama, kategori, hargabeli, hargajual, stok, minstok);
            refreshTable();
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed

