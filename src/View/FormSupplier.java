    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Id = txtKode.getText().trim();
        nama = txtNamaSupplier.getText().trim();
        alamat = txtAlamat.getText().trim();
        email = txtEmail.getText().trim();
        telepon = txtTelepon.getText().trim();

        if (nama.equals("")) {
            JOptionPane.showMessageDialog(null, "Tidak Boleh Kosong");
            txtNamaSupplier.requestFocus();
        } else if (alamat.equals("")) {
            JOptionPane.showMessageDialog(null, "Tidak Boleh Kosong");
            txtAlamat.requestFocus();
        } else if (email.equals("")) {
            JOptionPane.showMessageDialog(null, "Tidak Boleh Kosong");
            txtEmail.requestFocus();
        } else if (telepon.equals("")) {
            JOptionPane.showMessageDialog(null, "Tidak Boleh Kosong");
            txtTelepon.requestFocus();
        } else {
            spl.Save(Id, nama, alamat, email, telepon);
            refreshTable();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

